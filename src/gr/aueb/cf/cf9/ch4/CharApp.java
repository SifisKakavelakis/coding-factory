package gr.aueb.cf.cf9.ch4;

public class CharApp {

    public static void main(String[] args) {

        char char1 = 'A';
        char char2 = '*';

        System.out.println("ch1 = " + char1);
        System.out.println("ch2 = " + char2);

        System.out.printf("ch1 = %c, ordinal = %d\n", char1, (int) char1);
        System.out.printf("ch2 = %c, ordinal = %d\n", char2, (int) char2);


    }
}
